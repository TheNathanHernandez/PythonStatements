from ess import ask 

number = ask("Choose a number between 1 and 5.")

if number == 1: 
    print("One.")

if number == 2:
    print("Two.")

if number == 3:
    print("Three.")

if number == 4: 
    print("Four.")

if number == 5: 
    print("Five.")
  